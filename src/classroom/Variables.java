package classroom;

public class Variables {
    public static void main(String[] args) {

        // Variables: int
        int maxGrade = 10;
        int numberOfMembers = 15;
        int daysInMarch = 31;

        System.out.println(maxGrade);
        System.out.println(numberOfMembers);
        System.out.println(daysInMarch);

        // Variables: double
        double age = 3.6;
        double price = 10.95;
        double catWeight = 4.7;

        System.out.println(age);
        System.out.println(price);
        System.out.println(catWeight);

        // Variables: float
        float length = 8.6f;
        float weight = 34.6f;

        System.out.println(length);
        System.out.println(weight);

        // Chars
        char M = 115;
        char A = 41;
        char Y = 59;

        System.out.println(M);
        System.out.println(A);
        System.out.println(Y);

        // Boolean
        boolean isNight = true;
        boolean isMonday = false;
        boolean isMarch = true;

        System.out.println(isNight);
        System.out.println(isMonday);
        System.out.println(isMarch);
    }
}
